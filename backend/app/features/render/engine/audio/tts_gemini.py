"""Gemini TTS adapter — cloud expressive narration synthesis (preview).

Gemini 3.1 Flash TTS produces natural, emotionally-directed speech that
Edge-TTS's neural voices cannot match ("robot voice" complaint). It is
an ONLINE engine on a PREVIEW model, so it is strictly opt-in
(tts_engine="gemini") and the dispatcher in tts.py always falls back to
the default Edge → Piper → XTTS chain on any failure — a Gemini outage,
missing SDK, or exhausted quota never loses a render's narration.

Design mirrors tts_piper.py:
  - Hash-based synthesis cache (TEMP_DIR/gemini_tts_cache/) — identical
    text never hits the API twice, which also protects preview quota.
  - Raw PCM (s16le / 24 kHz / mono) → WAV → MP3 via FFmpeg so the output
    contract matches Edge/Piper/XTTS.
  - Raises RuntimeError on any failure — the caller falls back.

Two Gemini-specific notes:
  - Delivery is steered by a natural-language style instruction prefixed
    to the text (resolved per content_type; GEMINI_TTS_STYLE overrides).
    The ``rate`` knob used by Edge does not apply — the model paces itself.
  - All generated audio carries Google's inaudible SynthID watermark
    (detectable as AI-generated by platforms). Acceptable for narration;
    documented so nobody is surprised.
"""
