# Thread-safe synthesis cache — key → MP3 path.
_GEMINI_SYNTHESIS_CACHE: dict[str, str] = {}
_GEMINI_CACHE_LOCK = threading.Lock()


def _resolve_api_key() -> str:
    """Return a Gemini API key: rotation pool first, then config/env.
    Never raises — returns '' when unset (caller treats as unavailable)."""
    try:
        from app.features.render.ai.llm.key_pool import active_key
        key = active_key()
        if key:
            return key
    except Exception:
        pass
    try:
        from app.core import config as _cfg
        key = getattr(_cfg, "GEMINI_API_KEY", "")
        if key:
            return key
    except Exception:
        pass
    return os.environ.get("GEMINI_API_KEY", "")


def gemini_tts_available() -> bool:
    """True when the google-genai SDK is importable AND an API key exists.

    Never raises — used by the dispatcher to decide whether Gemini TTS is
    viable before attempting synthesis.
    """
    try:
        if importlib.util.find_spec("google.genai") is None:
            return False
        return bool(_resolve_api_key())
    except Exception:
        return False


def _resolve_voice(gender: str) -> str:
    env = (os.environ.get("GEMINI_TTS_VOICE") or "").strip()
    if env:
        return env
    g = str(gender or "").strip().lower()
    return _VOICE_BY_GENDER.get(g, _VOICE_BY_GENDER["default"])


def _lang_prefix(language: str) -> str:
    return str(language or "").strip().lower().split("-")[0]


def _rate_to_tempo_hint(rate: str, lang: str) -> str:
    """Map an explicit creator rate ("+10%" / "-8%") to a tempo direction.

    Content-type styles already carry a baseline tempo, so a hint is added
    only for a deliberate creator override (|rate| ≥ 3%) — never for the
    "+0%" default. Returns '' when no hint applies. Never raises."""
    try:
        pct = int(str(rate or "").strip().replace("%", "").replace("+", "") or 0)
    except Exception:
        return ""
    hints = _TEMPO_HINTS.get(lang, _TEMPO_HINTS["en"])
    if pct >= 8:
        return hints[0]
    if pct >= 3:
        return hints[1]
    if pct <= -8:
        return hints[2]
    if pct <= -3:
        return hints[3]
    return ""


def _resolve_style(content_type: str, language: str = "", rate: str = "") -> str:
    env = (os.environ.get("GEMINI_TTS_STYLE") or "").strip()
    if env:
        return env
    lang = _lang_prefix(language)
    table = _STYLE_TABLES.get(lang, _STYLE_BY_CONTENT_TYPE_EN)
    style = table.get(str(content_type or "").strip().lower(), table["vlog"])
    hint = _rate_to_tempo_hint(rate, lang)
    return f"{style}. {hint}" if hint else style


def _wav_to_mp3(wav_path: Path, mp3_path: Path) -> None:
    """Convert WAV to MP3 via FFmpeg (libmp3lame, VBR quality 2 ≈ 190 kbps)."""
    from app.services.bin_paths import get_ffmpeg_bin
    cmd = [
        get_ffmpeg_bin(), "-y",
        "-i", str(wav_path),
        "-codec:a", "libmp3lame", "-q:a", "2",
        str(mp3_path),
    ]
    result = subprocess.run(cmd, capture_output=True, timeout=30)
    if result.returncode != 0:
        raise RuntimeError(
            f"WAV→MP3 conversion failed: {result.stderr.decode(errors='replace')[:300]}"
        )


def _synthesis_cache_key(text: str, language: str, voice: str, style: str) -> str:
    """16-char SHA256 prefix — deterministic key for synthesized audio.
    Model id is part of the key so a model upgrade never serves stale audio."""
    payload = f"gemini\x00{_GEMINI_TTS_MODEL}\x00{voice}\x00{style}\x00{language}\x00{text}"
    return hashlib.sha256(payload.encode("utf-8")).hexdigest()[:16]


def synthesize_gemini(
    *,
    text: str,
    language: str,
    gender: str,
    job_id: str,
    content_type: str = "vlog",
    output_path: str | None = None,
    rate: str = "",
) -> str:
    """Synthesize speech with Gemini TTS (online, preview). Returns MP3 path.

    Raises RuntimeError on any failure — the caller must catch and fall
    back to the Edge → Piper → XTTS chain. Uses a hash-based cache:
    identical (text, language, voice, style, model) returns instantly and
    spends no API quota.
    """
    from app.core.config import TEMP_DIR

    clean_text = str(text or "").strip()
    if not clean_text:
        raise RuntimeError("Narration text is empty")

    api_key = _resolve_api_key()
    if not api_key:
        raise RuntimeError("gemini_tts_no_api_key")

    voice = _resolve_voice(gender)
    # Style (incl. any tempo hint) is part of the cache key downstream, so a
    # rate change correctly re-synthesizes instead of serving cached audio.
    style = _resolve_style(content_type, language=language, rate=rate)

    cache_key = _synthesis_cache_key(clean_text, language, voice, style)
    cache_dir = TEMP_DIR / "gemini_tts_cache"
    cached_mp3 = cache_dir / f"{cache_key}.mp3"
    with _GEMINI_CACHE_LOCK:
        if cached_mp3.exists() and cached_mp3.stat().st_size > 0:
            dest = Path(output_path) if output_path else (TEMP_DIR / job_id / "voice" / "narration.mp3")
            dest.parent.mkdir(parents=True, exist_ok=True)
            if dest != cached_mp3:
                shutil.copy2(str(cached_mp3), str(dest))
            logger.info("gemini_tts_cache_hit job_id=%s key=%s", job_id, cache_key)
            return str(dest)

    work_dir = TEMP_DIR / job_id / "voice"
    work_dir.mkdir(parents=True, exist_ok=True)
    wav_path = work_dir / "narration.gemini.wav"
    mp3_path = Path(output_path) if output_path else work_dir / "narration.mp3"
    mp3_path.parent.mkdir(parents=True, exist_ok=True)

    start = time.perf_counter()
    logger.info(
        "gemini_tts_start job_id=%s model=%s voice=%s language=%s content_type=%s chars=%d",
        job_id, _GEMINI_TTS_MODEL, voice, language, content_type, len(clean_text),
    )

    from google import genai  # lazy — optional dependency
    from google.genai import types as genai_types

    client = genai.Client(
        api_key=api_key,
        http_options={"timeout": _GEMINI_TTS_TIMEOUT_SEC * 1000},
    )
    resp = client.models.generate_content(
        model=_GEMINI_TTS_MODEL,
        contents=f"{style}: {clean_text}",
        config=genai_types.GenerateContentConfig(
            response_modalities=["AUDIO"],
            speech_config=genai_types.SpeechConfig(
                voice_config=genai_types.VoiceConfig(
                    prebuilt_voice_config=genai_types.PrebuiltVoiceConfig(voice_name=voice)
                )
            ),
        ),
    )

    try:
        pcm = resp.candidates[0].content.parts[0].inline_data.data
    except (AttributeError, IndexError, TypeError) as exc:
        raise RuntimeError(f"gemini_tts_empty_response: {exc}") from exc
    if not pcm:
        raise RuntimeError("gemini_tts_empty_audio")

    with wave.open(str(wav_path), "wb") as wf:
        wf.setnchannels(1)
        wf.setsampwidth(2)
        wf.setframerate(_GEMINI_TTS_SAMPLE_RATE)
        wf.writeframes(pcm)

    if not wav_path.exists() or wav_path.stat().st_size <= 0:
        raise RuntimeError("Gemini TTS produced empty WAV output")

    _wav_to_mp3(wav_path, mp3_path)
    wav_path.unlink(missing_ok=True)

    if not mp3_path.exists() or mp3_path.stat().st_size <= 0:
        raise RuntimeError("Gemini TTS WAV→MP3 conversion produced empty file")

    # Store in shared cache for future reuse.
    try:
        cache_dir.mkdir(parents=True, exist_ok=True)
        shutil.copy2(str(mp3_path), str(cached_mp3))
        with _GEMINI_CACHE_LOCK:
            _GEMINI_SYNTHESIS_CACHE[cache_key] = str(cached_mp3)
    except Exception as _cache_exc:
        logger.debug("gemini_tts_cache_write_failed key=%s: %s", cache_key, _cache_exc)

    audio_sec = len(pcm) / (_GEMINI_TTS_SAMPLE_RATE * 2)
    elapsed_ms = int((time.perf_counter() - start) * 1000)
    logger.info(
        "gemini_tts_complete job_id=%s model=%s voice=%s audio_sec=%.1f elapsed_ms=%d output=%s",
        job_id, _GEMINI_TTS_MODEL, voice, audio_sec, elapsed_ms, mp3_path.name,
    )
    return str(mp3_path)
