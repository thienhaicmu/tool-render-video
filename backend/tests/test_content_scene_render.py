@needs_ffmpeg
def test_render_content_scene_composites_av(tmp_path):
    narration_dur = 3.0
    audio = tmp_path / "narr.mp3"
    _make_silent_audio(str(audio), narration_dur, sr=48000)

    scene = ContentScene(
        index=0, role="hook",
        narration="Xin chao cac ban. Hom nay chung ta tim hieu ve Sao Hoa. That thu vi!",
        emotion="curious", reading_speed=1.0, pause_before=0.4, pause_after=0.6,
    )
    out = tmp_path / "scene_000.mp4"
    ok = csr.render_content_scene(
        scene=scene, background_kind="color", background_value="#000000",
        narration_audio_path=str(audio), narration_dur=narration_dur,
        width=320, height=568, fps=30, sample_rate=48000,
        out_path=str(out), work_dir=str(tmp_path / "work"), subtitle_enabled=True,
    )
    assert ok and out.exists(), "scene render must succeed"
    info = _probe(str(out))
    assert info["v"] is True, "scene must have a video stream"
    assert info["a"] is True, "scene must have an audio stream"
    expected = 0.4 + narration_dur + 0.6  # pause_before + narration + pause_after
    assert abs(info["dur"] - expected) <= 0.6, f"scene duration {info['dur']} vs ~{expected}"


