# ── Skip guard ───────────────────────────────────────────────────────────────

def _ffmpeg_ok() -> bool:
    try:
        from app.services.bin_paths import get_ffmpeg_bin
        r = subprocess.run([get_ffmpeg_bin(), "-version"], capture_output=True, timeout=5)
        return r.returncode == 0
    except Exception:
        return False


_FFMPEG = _ffmpeg_ok()
needs_ffmpeg = pytest.mark.skipif(not _FFMPEG, reason="FFmpeg not available on PATH")


def _probe(path: str) -> dict:
    """Return {'v': bool, 'a': bool, 'dur': float} for a media file via ffprobe."""
    from app.services.bin_paths import get_ffprobe_bin
    def _q(stream: str, entries: str) -> str:
        out = subprocess.run(
            [get_ffprobe_bin(), "-v", "error", "-select_streams", stream,
             "-show_entries", entries, "-of", "default=noprint_wrappers=1:nokey=1", path],
            capture_output=True, text=True, timeout=30,
        )
        return (out.stdout or "").strip()
    has_v = bool(_q("v:0", "stream=codec_type"))
    has_a = bool(_q("a:0", "stream=codec_type"))
    dur_raw = _q("v:0", "stream=duration") or ""
    if not dur_raw:
        fout = subprocess.run(
            [get_ffprobe_bin(), "-v", "error", "-show_entries", "format=duration",
             "-of", "default=noprint_wrappers=1:nokey=1", path],
            capture_output=True, text=True, timeout=30,
        )
        dur_raw = (fout.stdout or "").strip()
    try:
        dur = float(dur_raw)
    except (TypeError, ValueError):
        dur = 0.0
    return {"v": has_v, "a": has_a, "dur": dur}


def _make_silent_audio(path: str, seconds: float, sr: int = 48000) -> None:
    from app.services.bin_paths import get_ffmpeg_bin
    subprocess.run(
        [get_ffmpeg_bin(), "-y", "-f", "lavfi",
         "-i", f"anullsrc=channel_layout=stereo:sample_rate={sr}",
         "-t", f"{seconds:.3f}", "-c:a", "libmp3lame", path],
        capture_output=True, check=True, timeout=60,
    )


# ── Pure-helper unit tests (no FFmpeg) ───────────────────────────────────────

def test_normalize_color():
    assert cbg._normalize_color("#1e90ff") == "0x1e90ff"
    assert cbg._normalize_color("#000000") == "0x000000"
    assert cbg._normalize_color("black") == "black"
    assert cbg._normalize_color("") == "0x000000"
    assert cbg._normalize_color("not-a-color!") == "not-a-color!"  # passthrough token
    assert cbg._normalize_color("#zzzzzz") == "0x000000"  # invalid hex → black


def test_reading_speed_to_rate():
    assert csr._reading_speed_to_rate(1.0) == "+0%"
    assert csr._reading_speed_to_rate(1.1) == "+10%"
    assert csr._reading_speed_to_rate(0.9) == "-10%"
    assert csr._reading_speed_to_rate(9.0) == "+100%"   # clamp 2.0 → +100%
    assert csr._reading_speed_to_rate("bad") == "+0%"


def test_build_scene_srt(tmp_path):
    srt = tmp_path / "s.srt"
    ok = csr._build_scene_srt(
        "First sentence here. Second one follows! And a third.", 0.5, 6.0, str(srt),
    )
    assert ok and srt.exists()
    body = srt.read_text(encoding="utf-8")
    assert "-->" in body
    assert "00:00:00,500 -->" in body  # honours the start offset
    # empty narration → no file / False
    assert csr._build_scene_srt("", 0.0, 3.0, str(tmp_path / "e.srt")) is False


def test_split_cues_caps_count():
    text = ". ".join(f"sentence number {i}" for i in range(20)) + "."
    cues = csr._split_cues(text, 6)
    assert 0 < len(cues) <= 6


