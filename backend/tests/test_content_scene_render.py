"""test_content_scene_render.py — Content Mode engine (Phase 2).

Verifies the two new offline building blocks that let Content Mode composite a
scene WITHOUT any source footage:

  - content_background.build_background_clip  → a video-only background clip
  - content_scene_render.render_content_scene → background + burned subtitle +
    muxed narration audio, at the target A/V spec

Runs FULLY OFFLINE: the narration audio is a synthetic silent track generated by
FFmpeg (lavfi anullsrc), so no network / TTS engine is required. Also unit-tests
the pure helpers (SRT builder, reading-speed→rate mapping, color normalisation)
which need no FFmpeg at all.

Skip guard: the FFmpeg-dependent tests are skipped when FFmpeg is not on PATH.
"""
